def add_par(style_name, text=""):

    p = doc.add_paragraph(text)

    try:

        p.style = doc.styles[style_name]

    except KeyError:

        pass

    return p





def add_figure(filename, width_cm=11.0):

    p = doc.add_paragraph()

    try:

        p.style = doc.styles["$_Рисунок"]

    except KeyError:

        pass

    run = p.add_run()

    run.add_picture(os.path.join(FIG_DIR, filename), width=Cm(width_cm))

    return p





def add_caption(text):

    add_par("$_Рисунок_название_с_номером", text)





def add_copyright_with_rule(text):

    """Копирайт на первой странице после титульного блока; сверху — горизонтальная черта."""

    p = doc.add_paragraph()

    try:

        p.style = doc.styles["$_Копирайт"]

    except KeyError:

        pass

    p.paragraph_format.space_before = Pt(8)

    p.paragraph_format.space_after = Pt(10)

    pPr = p._element.get_or_add_pPr()

    pBdr = OxmlElement("w:pBdr")

    top = OxmlElement("w:top")

    top.set(qn("w:val"), "single")

    top.set(qn("w:sz"), "18")

    top.set(qn("w:space"), "4")

    top.set(qn("w:color"), "000000")

    pBdr.append(top)

    pPr.append(pBdr)

    p.add_run(text)





